def get_large_glyphs(text, alphabet=MedAlphabet):
    result = ''
    for line in range(len(alphabet['A'])):
        for char in text.upper():
            if char in alphabet:
                result += alphabet[char][line] + ' '
            else:
                result += ''  # Add spaces if character not found
        result += '\n'  # Move to the next line
    return result